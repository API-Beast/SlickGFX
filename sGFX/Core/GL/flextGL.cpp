bool flextInit(GLFWwindow* window)
{
    int major = glfwGetWindowAttrib(window, GLFW_CONTEXT_VERSION_MAJOR);
    int minor = glfwGetWindowAttrib(window, GLFW_CONTEXT_VERSION_MINOR);

    flextLoadOpenGLFunctions();

    /* Check for minimal version and profile */

    if(major * 10 + minor < 45) {
        fprintf(stderr, "Error: OpenGL version 4.5 not supported.\n");
        fprintf(stderr, "       Your version is %d.%d.\n", major, minor);
        fprintf(stderr, "       Try updating your graphics driver.\n");
        return false;
    }

    /* Check for extensions */

    if(!glfwExtensionSupported("GL_ARB_shading_language_include")) {
        fprintf(stderr, "Error: OpenGL extension ARB_shading_language_include not supported.\n");
        fprintf(stderr, "       Try updating your graphics driver.\n");
        return false;
    }

    return true;
}

